#!/bin/bash
## -----------------------------------------------------------------------------
## Linux Scripts.
## Output apps' version.
##
## @package ojullien\bash\bin\version
## @license MIT <https://github.com/ojullien/bash-version/blob/master/LICENSE>
## -----------------------------------------------------------------------------
#set -o errexit
set -o nounset
set -o pipefail

## -----------------------------------------------------------------------------
## Shell scripts directory, eg: /root/work/Shell/src/bin
## -----------------------------------------------------------------------------
readonly m_DIR_REALPATH="$(realpath "$(dirname "$0")")"

## -----------------------------------------------------------------------------
## Load constants
## -----------------------------------------------------------------------------
# shellcheck source=/dev/null
. "${m_DIR_REALPATH}/../sys/constant.sh"

## -----------------------------------------------------------------------------
## Start
## -----------------------------------------------------------------------------
for F in `find "${m_DIR_APP}" -type f -name VERSION`
do
    echo -n ${F##*app/}:
    echo $(cat $F)
done


## -----------------------------------------------------------------------------
## END
## -----------------------------------------------------------------------------
exit 0
